package Game;

public class Difficulty {
	public boolean easy = false;
	public boolean normal = false;
	public boolean hard = false;
	public boolean crazy = false;
	
	public Difficulty(boolean easy, boolean normal, boolean hard, boolean crazy) {
		super();
		this.easy = easy;
		this.normal = normal;
		this.hard = hard;
		this.crazy = crazy;
	}
}
